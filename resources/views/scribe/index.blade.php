                    <h2 id="endpoints-GETapi-categories-search">Search Resources</h2>

<p>
<small class="badge badge-darkred">requires authentication</small>
</p>

<p>Allows searching for data in this resource using multiple options.</p>
<p>Options for searching</p>
<ul>
<li>By field name: e.g. <code>?name=John</code> - Specific search</li>
<li>By field name with <code>LIKE</code> operator: e.g. <code>?name_like=John</code> - Fuzzy search</li>
<li>By field name with <code>!=</code> operator: e.g. <code>?age_not=5</code></li>
<li>By field name with <code>&gt;</code> or <code>&lt;</code> operator: e.g. <code>?age_gt=5</code> or <code>?age_lt=10</code></li>
<li>By field name with <code>&gt;=</code> or <code>&lt;=</code> operator: e.g. <code>?age_gte=5</code> or <code>?age_lte=10</code></li>
<li>By field name with <code>IN</code> or <code>NOT IN</code> operator: e.g. <code>?id_in=1,3,5</code> or <code>?id_notIn=2,4</code></li>
<li>By field name with <code>NULL</code> or <code>NOT NULL</code> operator: e.g. <code>?email_isNull</code> or <code>?email_isNotNull</code></li>
</ul>

